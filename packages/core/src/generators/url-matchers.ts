import { getParamsInPath } from '../getters/params';
import {
  type ClientFileBuilder,
  type ContextSpec,
  type GeneratorVerbOptions,
  type NormalizedOutputOptions,
  type NormalizedUrlMatchersOptions,
  OutputMode,
} from '../types';
import { escapeRegExp, isFunction, jsDoc, kebab, upath } from '../utils';
import { getFileInfo } from '../utils/file';

const getPrimaryTag = (verbOption: GeneratorVerbOptions): string =>
  kebab(verbOption.tags[0] ?? 'default');

const buildFileHeader = (
  output: NormalizedOutputOptions,
  context: ContextSpec,
): string => {
  if (output.override.header === false) {
    return '';
  }

  const info = context.spec.info ?? { title: 'API', version: '1.0.0' };
  const header = isFunction(output.override.header)
    ? output.override.header(info)
    : [
        'Generated by orval',
        'Do not edit manually.',
        ...(info.title ? [info.title] : []),
        ...(info.version ? [`OpenAPI spec version: ${info.version}`] : []),
      ];

  return Array.isArray(header) ? jsDoc({ description: header }) : header;
};

const shouldIncludeQuerySuffix = (
  options: NormalizedUrlMatchersOptions,
  hasQueryParams: boolean,
): boolean => {
  switch (options.querySuffix) {
    case 'always': {
      return true;
    }
    case 'never': {
      return false;
    }
    default: {
      return hasQueryParams;
    }
  }
};

/**
 * Builds the pattern body for a RegExp literal from an OpenAPI path (`pathRoute`).
 */
export function buildUrlMatcherRegexPattern(
  pathRoute: string,
  options: NormalizedUrlMatchersOptions,
  hasQueryParams: boolean,
): string {
  const params = getParamsInPath(pathRoute);
  let pattern = options.prefixCapture;
  let rest = pathRoute;

  for (const name of params) {
    const placeholder = `{${name}}`;
    const index = rest.indexOf(placeholder);
    if (index === -1) {
      continue;
    }
    pattern += escapeRegExp(rest.slice(0, index));
    pattern += `(${options.defaultParamPattern})`;
    rest = rest.slice(index + placeholder.length);
  }

  pattern += escapeRegExp(rest);

  if (shouldIncludeQuerySuffix(options, hasQueryParams)) {
    pattern += String.raw`(\?.*)?`;
  }

  pattern += '$';
  return pattern;
}

export function buildUrlMatcherRegexLiteral(
  pathRoute: string,
  options: NormalizedUrlMatchersOptions,
  hasQueryParams: boolean,
): string {
  const pattern = buildUrlMatcherRegexPattern(
    pathRoute,
    options,
    hasQueryParams,
  );
  const literalBody = pattern.replaceAll('/', String.raw`\/`);
  return `/${literalBody}/`;
}

export function getUrlMatcherExportName(
  operationName: string,
  options: NormalizedUrlMatchersOptions,
): string {
  return `${operationName}${options.exportSuffix}`;
}

const getUrlMatcherFilePath = (
  output: NormalizedOutputOptions,
  tag?: string,
): string => {
  const urlMatchers = output.urlMatchers;
  if (!urlMatchers) {
    throw new Error('urlMatchers is not configured');
  }

  const { dirname, filename } = getFileInfo(output.target, {
    extension: output.fileExtension,
  });
  const suffix = urlMatchers.fileExtension.startsWith('.')
    ? urlMatchers.fileExtension
    : `.${urlMatchers.fileExtension}`;

  switch (output.mode) {
    case OutputMode.TAGS: {
      const normalizedTag = kebab(tag ?? 'default');
      return upath.joinSafe(dirname, `${normalizedTag}${suffix}`);
    }
    case OutputMode.TAGS_SPLIT: {
      const normalizedTag = kebab(tag ?? 'default');
      return upath.joinSafe(
        dirname,
        normalizedTag,
        `${normalizedTag}${suffix}`,
      );
    }
    default: {
      return upath.joinSafe(dirname, `${filename}${suffix}`);
    }
  }
};

const buildUrlMatcherFileContent = (
  verbOptions: Record<string, GeneratorVerbOptions>,
  output: NormalizedOutputOptions,
  context: ContextSpec,
): string => {
  const urlMatchers = output.urlMatchers;
  if (!urlMatchers) {
    return '';
  }

  const exports = Object.values(verbOptions)
    .toSorted((a, b) => a.operationName.localeCompare(b.operationName))
    .map((verbOption) => {
      const exportName = getUrlMatcherExportName(
        verbOption.operationName,
        urlMatchers,
      );
      const literal = buildUrlMatcherRegexLiteral(
        verbOption.pathRoute,
        urlMatchers,
        Boolean(verbOption.queryParams),
      );
      return `export const ${exportName} = ${literal};`;
    })
    .join('\n\n');

  const header = buildFileHeader(output, context);
  return exports.length > 0 ? `${header}${exports}\n` : header;
};

const buildUrlMatcherFile = (
  verbOptions: Record<string, GeneratorVerbOptions>,
  outputPath: string,
  output: NormalizedOutputOptions,
  context: ContextSpec,
): ClientFileBuilder => ({
  path: outputPath,
  content: buildUrlMatcherFileContent(verbOptions, output, context),
});

/**
 * Generates sibling files with `RegExp` exports for matching HTTP request URLs.
 */
export function generateUrlMatcherExtraFiles(
  verbOptions: Record<string, GeneratorVerbOptions>,
  output: NormalizedOutputOptions,
  context: ContextSpec,
): ClientFileBuilder[] {
  if (!output.urlMatchers || Object.keys(verbOptions).length === 0) {
    return [];
  }

  if (
    output.mode === OutputMode.TAGS ||
    output.mode === OutputMode.TAGS_SPLIT
  ) {
    const groupedVerbOptions = new Map<
      string,
      Record<string, GeneratorVerbOptions>
    >();

    for (const verbOption of Object.values(verbOptions)) {
      const tag = getPrimaryTag(verbOption);
      const currentGroup = groupedVerbOptions.get(tag) ?? {};
      currentGroup[verbOption.operationId] = verbOption;
      groupedVerbOptions.set(tag, currentGroup);
    }

    return [...groupedVerbOptions.entries()].map(([tag, tagVerbOptions]) =>
      buildUrlMatcherFile(
        tagVerbOptions,
        getUrlMatcherFilePath(output, tag),
        output,
        context,
      ),
    );
  }

  return [
    buildUrlMatcherFile(
      verbOptions,
      getUrlMatcherFilePath(output),
      output,
      context,
    ),
  ];
}
